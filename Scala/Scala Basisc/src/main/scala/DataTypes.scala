object DataTypes{
  def main(args: Array[String]): Unit = {
    // Unit: It used to specify void functions that won't return anything
    // Primitive Data Types
    var age: Int = 25 // Integer


    var myFloat: Float = 78.12 // Float
    var price: Double = 123.4545 // Double
    var longIntVal: Long = 1232134178L // Long Integer

    var myChar: Char = 'v' // Character
    var message: String = "Hello, World!" // String

    var byteValue: Byte = 127 // Byte
    var boolVal: Boolean = true // Boolean
  }
}
